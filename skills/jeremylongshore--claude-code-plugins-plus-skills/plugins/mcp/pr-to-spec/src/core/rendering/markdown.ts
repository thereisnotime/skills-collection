import type { PromptSpec } from "../schema/prompt-spec.js";

/**
 * Render a PromptSpec as a human-readable Markdown summary.
 */
export function renderMarkdown(spec: PromptSpec): string {
	const lines: string[] = [];

	lines.push(`# Prompt Spec: ${spec.title}`);
	lines.push("");
	lines.push(
		`> Generated by [pr-to-spec](https://github.com/jeremylongshore/pr-to-spec) at ${spec.generated_at}`,
	);
	lines.push("");

	// Source
	lines.push("## Source");
	lines.push(`- **Repo:** ${spec.source.repo}`);
	if (spec.source.pr_number !== undefined && spec.source.pr_url) {
		lines.push(`- **PR:** [#${spec.source.pr_number}](${spec.source.pr_url})`);
	}
	lines.push(`- **Author:** @${spec.source.author}`);
	lines.push(`- **Branch:** \`${spec.source.head_branch}\` → \`${spec.source.base_branch}\``);
	lines.push("");

	// Summary
	lines.push("## Summary");
	lines.push(spec.summary);
	lines.push("");

	// Intent
	lines.push("## Intent");
	lines.push(`- **Type:** ${spec.intent.change_type}`);
	lines.push(`- **Goal:** ${spec.intent.likely_goal}`);
	lines.push("");

	// Stats
	lines.push("## Stats");
	lines.push("| Metric | Value |");
	lines.push("|--------|-------|");
	lines.push(`| Files changed | ${spec.stats.files_changed} |`);
	lines.push(`| Additions | +${spec.stats.additions} |`);
	lines.push(`| Deletions | -${spec.stats.deletions} |`);
	lines.push(`| Commits | ${spec.stats.commits} |`);
	lines.push("");

	// Affected Files
	lines.push("## Affected Files");
	for (const f of spec.affected_files) {
		const badge = statusBadge(f.status);
		lines.push(`- ${badge} \`${f.filename}\` (+${f.additions}/-${f.deletions})`);
	}
	lines.push("");

	// Scope
	lines.push("## Scope");
	lines.push("**Include:**");
	for (const s of spec.scope.include) {
		lines.push(`- ${s}`);
	}
	if (spec.scope.exclude.length > 0) {
		lines.push("**Exclude:**");
		for (const s of spec.scope.exclude) {
			lines.push(`- ${s}`);
		}
	}
	lines.push("");

	// Semantic Changes
	if (spec.semantic_changes && spec.semantic_changes.length > 0) {
		lines.push("## Semantic Changes");
		const grouped = new Map<string, typeof spec.semantic_changes>();
		for (const c of spec.semantic_changes) {
			const key = c.type;
			const arr = grouped.get(key) ?? [];
			arr.push(c);
			grouped.set(key, arr);
		}
		for (const [type, items] of grouped) {
			lines.push(`**${type}s:**`);
			for (const item of items.slice(0, 10)) {
				const icon = item.action === "added" ? "+" : item.action === "removed" ? "-" : "~";
				lines.push(`- ${icon} \`${item.name}\` (${item.file})`);
			}
		}
		lines.push("");
	}

	// Monorepo
	if (spec.monorepo?.detected) {
		lines.push("## Monorepo");
		lines.push(`**Affected packages:** ${spec.monorepo.affected_packages.join(", ")}`);
		if (spec.monorepo.workspace_root) {
			lines.push(`**Workspace root:** ${spec.monorepo.workspace_root}`);
		}
		lines.push("");
	}

	// Review Summary
	if (spec.review_summary) {
		lines.push("## Review Summary");
		lines.push(`- **Status:** ${spec.review_summary.approval_status}`);
		lines.push(`- **Reviewers:** ${spec.review_summary.reviewers.join(", ") || "none"}`);
		lines.push(`- **Comments:** ${spec.review_summary.total_comments}`);
		if (spec.review_summary.key_concerns.length > 0) {
			lines.push("");
			lines.push("**Key Concerns:**");
			for (const c of spec.review_summary.key_concerns.slice(0, 5)) {
				lines.push(`- ${c}`);
			}
		}
		if (spec.review_summary.file_discussions.length > 0) {
			lines.push("");
			lines.push("**Most Discussed Files:**");
			for (const fd of spec.review_summary.file_discussions.slice(0, 5)) {
				lines.push(`- \`${fd.file}\` (${fd.comment_count} comments)`);
			}
		}
		lines.push("");
	}

	// Risk Flags
	if (spec.risk_flags.length > 0) {
		lines.push("## Risk Flags");
		for (const r of spec.risk_flags) {
			const icon = r.severity === "high" ? "!!!" : r.severity === "medium" ? "!!" : "!";
			lines.push(`- **[${r.severity.toUpperCase()}]** ${icon} ${r.category}: ${r.description}`);
		}
		lines.push("");
	}

	// Constraints
	if (spec.constraints.length > 0) {
		lines.push("## Constraints");
		for (const c of spec.constraints) {
			lines.push(`- ${c}`);
		}
		lines.push("");
	}

	// Acceptance Criteria
	lines.push("## Acceptance Criteria");
	for (const c of spec.acceptance_criteria) {
		lines.push(`- [ ] ${c}`);
	}
	lines.push("");

	// Verification
	lines.push("## Verification");
	lines.push("**Tests required:**");
	for (const t of spec.verification.tests_required) {
		lines.push(`- ${t}`);
	}
	if (spec.verification.manual_checks.length > 0) {
		lines.push("**Manual checks:**");
		for (const m of spec.verification.manual_checks) {
			lines.push(`- ${m}`);
		}
	}
	lines.push("");

	// Open Questions
	if (spec.open_questions.length > 0) {
		lines.push("## Open Questions");
		for (const q of spec.open_questions) {
			lines.push(`- ${q}`);
		}
		lines.push("");
	}

	// AI Enhanced
	if (spec.ai_enhanced) {
		lines.push("## AI-Enhanced Analysis");
		lines.push(`> Provider: ${spec.ai_enhanced.provider}`);
		lines.push("");
		lines.push(`**Summary:** ${spec.ai_enhanced.summary}`);
		lines.push("");
		lines.push(`**Goal:** ${spec.ai_enhanced.goal}`);
		lines.push("");
		if (spec.ai_enhanced.key_changes.length > 0) {
			lines.push("**Key Changes:**");
			for (const c of spec.ai_enhanced.key_changes) {
				lines.push(`- ${c}`);
			}
			lines.push("");
		}
		if (spec.ai_enhanced.review_hints.length > 0) {
			lines.push("**Review Hints:**");
			for (const h of spec.ai_enhanced.review_hints) {
				lines.push(`- ${h}`);
			}
			lines.push("");
		}
	}

	// Generation Prompt
	lines.push("## Generation Prompt");
	lines.push("```");
	lines.push(spec.generation_prompt);
	lines.push("```");
	lines.push("");

	// Decision Prompt
	lines.push("## Decision Prompt");
	lines.push("```");
	lines.push(spec.decision_prompt);
	lines.push("```");
	lines.push("");

	return lines.join("\n");
}

function statusBadge(status: string): string {
	switch (status) {
		case "added":
			return "[A]";
		case "removed":
			return "[D]";
		case "modified":
			return "[M]";
		case "renamed":
			return "[R]";
		case "copied":
			return "[C]";
		default:
			return "[?]";
	}
}
