#!/usr/bin/env node
/**
 * Update Metrics — Surgically updates plugin/skill counts in:
 *   - README.md badge numbers
 *   - GitHub repo description
 *   - Homepage description in BaseLayout.astro
 *
 * Usage:
 *   node scripts/update-metrics.mjs [--dry-run]
 *
 * Runs weekly via .github/workflows/update-metrics.yml
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, '..');

const DRY_RUN = process.argv.includes('--dry-run');

console.log(`\nMetrics Update${DRY_RUN ? ' (DRY RUN)' : ''}\n${'='.repeat(40)}\n`);

// ── 1. Count plugins from catalog ────────────────────────────────
const catalogPath = join(REPO_ROOT, '.claude-plugin/marketplace.extended.json');
const catalog = JSON.parse(readFileSync(catalogPath, 'utf-8'));
const pluginCount = catalog.plugins.length;

// ── 2. Count skills from unified search index (website source of truth) ──
// The unified search index is generated by the build pipeline (discover-skills.mjs)
// and represents the exact counts shown on the website.
const searchIndexPath = join(REPO_ROOT, 'marketplace/src/data/unified-search-index.json');
let skillCount;
if (existsSync(searchIndexPath)) {
  const searchIndex = JSON.parse(readFileSync(searchIndexPath, 'utf-8'));
  skillCount = searchIndex.stats.totalSkills;
} else {
  // Fallback: count from skills catalog
  const skillsCatalogPath = join(REPO_ROOT, 'marketplace/src/data/skills-catalog.json');
  if (existsSync(skillsCatalogPath)) {
    const skillsCatalog = JSON.parse(readFileSync(skillsCatalogPath, 'utf-8'));
    skillCount = skillsCatalog.count;
  } else {
    console.error('ERROR: No skills data found. Run marketplace build first.');
    process.exit(1);
  }
}

// ── 3. Count categories ──────────────────────────────────────────
const categories = [...new Set(catalog.plugins.map(p => p.category))];
const categoryCount = categories.length;

console.log(`Plugins:    ${pluginCount}`);
console.log(`Skills:     ${skillCount}`);
console.log(`Categories: ${categoryCount}\n`);

// ── 4. Update README.md badges ───────────────────────────────────
const readmePath = join(REPO_ROOT, 'README.md');
let readme = readFileSync(readmePath, 'utf-8');
let readmeChanged = false;

// Update Agent Skills badge: Agent%20Skills-NNNN%20Skills
const skillBadgeRe = /(Agent%20Skills-)\d+(%20Skills)/;
if (skillBadgeRe.test(readme)) {
  const before = readme.match(skillBadgeRe)[0];
  readme = readme.replace(skillBadgeRe, `$1${skillCount}$2`);
  if (before !== readme.match(skillBadgeRe)?.[0]) {
    console.log(`README: Agent Skills badge ${before} -> ${skillCount}`);
    readmeChanged = true;
  } else {
    console.log(`README: Agent Skills badge already ${skillCount}`);
  }
}

// Update Total Plugins badge: Total%20Plugins-NNN
const pluginBadgeRe = /(Total%20Plugins-)\d+/;
if (pluginBadgeRe.test(readme)) {
  const before = readme.match(pluginBadgeRe)[0];
  readme = readme.replace(pluginBadgeRe, `$1${pluginCount}`);
  if (before !== readme.match(pluginBadgeRe)?.[0]) {
    console.log(`README: Total Plugins badge ${before} -> ${pluginCount}`);
    readmeChanged = true;
  } else {
    console.log(`README: Total Plugins badge already ${pluginCount}`);
  }
}

if (readmeChanged) {
  if (!DRY_RUN) {
    writeFileSync(readmePath, readme, 'utf-8');
    console.log(`\nWrote updated README.md`);
  } else {
    console.log(`\nDRY RUN: Would update README.md`);
  }
} else {
  console.log(`\nREADME.md: No changes needed`);
}

// ── 5. Update GitHub repo description ────────────────────────────
const newDescription = `${pluginCount} plugins + ${skillCount} agent skills for Claude Code. Open-source marketplace with CCPI package manager, interactive tutorials, and production orchestration patterns.`;

// Read current description
let currentDescription = '';
try {
  currentDescription = execSync('gh repo view --json description -q .description', {
    cwd: REPO_ROOT,
    encoding: 'utf-8',
    timeout: 10000,
  }).trim();
} catch {
  console.log('Could not read current repo description (gh CLI not available or not authenticated)');
}

if (currentDescription && currentDescription !== newDescription) {
  console.log(`\nRepo description:`);
  console.log(`  Old: ${currentDescription}`);
  console.log(`  New: ${newDescription}`);
  if (!DRY_RUN) {
    try {
      execSync(`gh repo edit --description "${newDescription.replace(/"/g, '\\"')}"`, {
        cwd: REPO_ROOT,
        encoding: 'utf-8',
        timeout: 10000,
      });
      console.log(`  Updated!`);
    } catch (e) {
      console.log(`  Failed to update: ${e.message}`);
    }
  } else {
    console.log(`  DRY RUN: Would update`);
  }
} else if (currentDescription) {
  console.log(`\nRepo description: No change needed`);
}

// ── 6. Summary ───────────────────────────────────────────────────
console.log(`\n${'='.repeat(40)}`);
console.log(`Metrics update ${readmeChanged ? 'applied' : 'complete (no changes)'}${DRY_RUN ? ' (DRY RUN)' : ''}`);
console.log();
