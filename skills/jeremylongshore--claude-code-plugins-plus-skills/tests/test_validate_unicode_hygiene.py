"""Regression tests for scripts/validate-unicode-hygiene.py.

Runs the validator as a subprocess against the committed fixtures and asserts:

  * Each fixture file is detected at the severity its filename declares.
  * The clean fixture + the BOM-allowed fixture produce zero findings.
  * Default exit code is non-zero when any Blocker is present.
  * --warn-only always exits 0.
  * --strict elevates Major to a failing exit.

The fixtures live in tests/fixtures/unicode-hygiene/ and are regenerated by
the byte-precise Python script in that PR — do not hand-edit them.
"""

from __future__ import annotations

import pathlib
import subprocess
import sys
import unittest

REPO_ROOT = pathlib.Path(__file__).resolve().parent.parent
SCRIPT = REPO_ROOT / "scripts" / "validate-unicode-hygiene.py"
FIXTURES = REPO_ROOT / "tests" / "fixtures" / "unicode-hygiene"


def _run(*paths: pathlib.Path, extra: list[str] | None = None) -> subprocess.CompletedProcess[str]:
    cmd = [sys.executable, str(SCRIPT), *(extra or []), *(str(p) for p in paths)]
    return subprocess.run(cmd, capture_output=True, text=True, check=False)


class UnicodeHygieneValidator(unittest.TestCase):
    def test_clean_fixture_passes(self) -> None:
        result = _run(FIXTURES / "clean-skill.md")
        self.assertEqual(result.returncode, 0, result.stdout + result.stderr)
        self.assertIn("0 BLOCKER", result.stdout)
        self.assertIn("0 MAJOR", result.stdout)
        self.assertIn("0 MINOR", result.stdout)

    def test_bom_at_offset_zero_is_allowed(self) -> None:
        result = _run(FIXTURES / "bom-allowed.md")
        self.assertEqual(result.returncode, 0, result.stdout + result.stderr)
        self.assertIn("0 BLOCKER", result.stdout)
        self.assertIn("0 MAJOR", result.stdout)

    def test_tag_chars_are_blocker(self) -> None:
        result = _run(FIXTURES / "blocker-tag-chars.md")
        self.assertNotEqual(result.returncode, 0)
        self.assertIn("tag-character", result.stdout)
        self.assertIn("U+E0061", result.stdout)

    def test_bidi_override_is_blocker(self) -> None:
        result = _run(FIXTURES / "blocker-bidi-override.md")
        self.assertNotEqual(result.returncode, 0)
        self.assertIn("bidi-control", result.stdout)
        self.assertIn("U+202E", result.stdout)

    def test_zero_width_is_major_not_blocker(self) -> None:
        result = _run(FIXTURES / "major-zero-width.md")
        # Default mode blocks only on Blocker — Major alone exits 0.
        self.assertEqual(result.returncode, 0, result.stdout + result.stderr)
        self.assertIn("zero-width-or-format", result.stdout)
        self.assertIn("U+200B", result.stdout)
        self.assertIn("1 MAJOR", result.stdout)

    def test_homoglyph_install_is_minor(self) -> None:
        result = _run(FIXTURES / "minor-homoglyph-install.md")
        self.assertEqual(result.returncode, 0, result.stdout + result.stderr)
        self.assertIn("mixed-script-identifier", result.stdout)
        self.assertIn("1 MINOR", result.stdout)

    def test_warn_only_never_fails(self) -> None:
        result = _run(FIXTURES / "blocker-tag-chars.md", extra=["--warn-only"])
        self.assertEqual(result.returncode, 0, result.stdout + result.stderr)
        # Findings still reported, just non-fatal.
        self.assertIn("tag-character", result.stdout)

    def test_strict_elevates_major(self) -> None:
        result = _run(FIXTURES / "major-zero-width.md", extra=["--strict"])
        self.assertNotEqual(result.returncode, 0)
        self.assertIn("zero-width-or-format", result.stdout)


if __name__ == "__main__":
    unittest.main()
